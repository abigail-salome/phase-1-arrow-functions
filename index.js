// Function expression 'divide' that divides two numbers
const divide = function(a, b) {
    return a / b;
  };
  
  // Arrow function 'square' that squares a number
  const square = (x) => {
    return x * x;
  };
  
  // Arrow function 'add' that adds two numbers
  const add = (a, b) => {
    return a + b;
  };



